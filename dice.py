def print_values_for_generator():
    print([random.choice(Dice.values) for i in range(300)])


class Dice:
    """
    Possible dice values
    """
    values = [1, 2, 3]
    gen = None

    def __init__(self, gen_flag):
        if gen_flag == GEN1:
            self.gen = gen_dice()
        elif gen_flag == GEN2:
            self.gen = gen_dice2()
        elif gen_flag == GEN3:
            self.gen = gen_dice3()

    def roll_dice(self):
        if self.gen is None:
            return random.choice(self.values)
        return next(self.gen)
